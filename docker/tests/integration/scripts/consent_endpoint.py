# ── /consent/approve (POST) rate-limit tests ─────────────────────────────────

def test_consent_approve_rate_limit() -> bool:
    """
    Exhaust the standard rate-limit bucket on POST /consent/approve.

    The rate-limiter dependency runs before CSRF validation, so repeated
    requests with an invalid CSRF token will return 403 until the bucket is
    exhausted, after which they return 429.  We count the first 429 as the
    signal that the rate limiter fired correctly.
    """
    print("\n=== POST /consent/approve Rate Limit Test ===")
    # Any syntactically valid transaction_id is fine; rate-limit fires before
    # transaction or CSRF lookup.
    dummy_txn_id = "a" * 36    # UUID-length, within max_length=100

    for i in range(STANDARD_RATE_LIMIT_COUNT + 3):
        r = requests.post(
            CONSENT_APPROVE_URL,
            data={
                "transaction_id": dummy_txn_id,
                "csrf_token":     "invalid-csrf-token",
            },
        )
        print(f"  Request {i+1}: status={r.status_code}")
        if r.status_code == 429:
            print("  ✓ Rate limit triggered as expected")
            return True

    print("  ✗ Rate limit was NOT triggered after all requests")
    return False


def test_consent_approve_with_valid_csrf():
    """
    Obtain a real CSRF token via GET /consent and submit it to POST
    /consent/approve.  The expected outcome is 302 (redirect to upstream
    provider) if the transaction is still valid, or 400 if it has expired.

    This test validates that the endpoint behaves correctly with proper CSRF
    tokens – i.e. it does NOT return 403.
    """
    print("\n=== POST /consent/approve With Valid CSRF Token ===")
    session        = requests.Session()
    client         = register_client()
    transaction_id = get_transaction_id(client["client_id"])
    assert transaction_id, "Could not obtain a transaction_id via /authorize"

    csrf_token = get_csrf_token(session, transaction_id)
    assert csrf_token, "Could not extract CSRF token from /consent HTML"
    print(f"  CSRF token obtained: {csrf_token[:8]}...{csrf_token[-4:]}")

    r = session.post(
        CONSENT_APPROVE_URL,
        data={
            "transaction_id": transaction_id,
            "csrf_token":     csrf_token,
        },
        allow_redirects=False,
    )
    print(f"  Status: {r.status_code}")
    assert r.status_code in (302, 400), (
        f"Expected 302 (redirect to upstream) or 400 (expired txn), got {r.status_code}"
    )
    if r.status_code == 302:
        print(f"  ✓ Redirecting to upstream provider: {r.headers.get('Location', '')[:60]}...")
    else:
        print("  ✓ Transaction expired as expected (400)")



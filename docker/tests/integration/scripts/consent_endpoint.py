"""
Integration tests for the /consent (GET) and /consent/approve (POST) endpoints.

Tests cover:
  1. Rate limiting (DOS prevention) – endpoint-specific standard rate limit
  2. Payload / field-size limits (FastAPI 422 responses)
  3. Flood probe across multiple rate-limit windows

Flow dependency:
  /register  →  /authorize  →  /consent  →  /consent/approve

CSRF note for /consent/approve:
  The endpoint uses a session-based CSRF token generated by GET /consent.
  Correct testing of /consent/approve requires:
    1. A requests.Session() so the session cookie is carried across requests.
    2. GET /consent  – sets the session cookie and returns the CSRF token
       embedded in the HTML form.
    3. POST /consent/approve – submits the session cookie + CSRF token.

  For rate-limit and payload-size tests, a session is still used but CSRF
  validity is not a concern – the rate limiter runs before CSRF validation,
  so 403 (bad CSRF) is the "normal" failure, and 429 indicates the rate
  limiter fired first.

NOTE on 422 vs 429 ordering:
  See authorize_endpoint.py for the same caveat on dependency-resolution order.
"""

import os
import re
import time
from urllib.parse import parse_qs, urlparse
import base64
import hashlib
import secrets

import requests
from dotenv import load_dotenv

load_dotenv()

BASE_URL           = os.getenv("MCP_SERVER_PUBLIC_URL")
REGISTER_URL       = f"{BASE_URL}/register"
AUTHORIZE_URL      = f"{BASE_URL}/authorize"
CONSENT_URL        = f"{BASE_URL}/consent"
CONSENT_APPROVE_URL = f"{BASE_URL}/consent/approve"

# ── Configurable rate-limit parameters ───────────────────────────────────────
STANDARD_RATE_LIMIT_COUNT  = int(os.getenv("PRIVATE_OAUTH_STANDARD_RATE_LIMIT_COUNT",  "5"))
STANDARD_RATE_LIMIT_WINDOW = int(os.getenv("PRIVATE_OAUTH_STANDARD_RATE_LIMIT_WINDOW", "60"))

# ── Configurable field constraints (mirror remote_auth.py Query/Form annotations) ─
MAX_TRANSACTION_ID_LENGTH = 100   # Query(..., max_length=100) on /consent
                                   # Form(...,  max_length=100) on /consent/approve

# ── Fixed test values ─────────────────────────────────────────────────────────
VALID_REDIRECT_URI = "https://example.com/callback"
REGISTER_PAYLOAD = {
    "client_name": "ConsentTestClient",
    "redirect_uris": [VALID_REDIRECT_URI],
    "grant_types": ["authorization_code"],
    "response_types": ["code"],
}

_CSRF_RE = re.compile(
    r'<input[^>]+name=["\']csrf_token["\'][^>]+value=["\']([^"\']+)["\']'
    r'|<input[^>]+value=["\']([^"\']+)["\'][^>]+name=["\']csrf_token["\']',
    re.IGNORECASE,
)


# ── Shared helpers ────────────────────────────────────────────────────────────

def register_client() -> dict:
    """Register a fresh DCR client and return the full response JSON."""
    r = requests.post(REGISTER_URL, json=REGISTER_PAYLOAD)
    assert r.status_code == 200, f"Registration failed: {r.status_code} {r.text}"
    return r.json()


def make_pkce_pair(verifier_length: int = 64) -> tuple[str, str]:
    """Return (code_verifier, code_challenge) for S256 PKCE."""
    code_verifier  = secrets.token_urlsafe(verifier_length)[:verifier_length]
    digest         = hashlib.sha256(code_verifier.encode("ascii")).digest()
    code_challenge = base64.urlsafe_b64encode(digest).rstrip(b"=").decode("ascii")
    return code_verifier, code_challenge


def get_transaction_id(client_id: str) -> str | None:
    """
    Call /authorize with valid params and parse the transaction_id from the
    302 Location header (redirect to /consent?transaction_id=<uuid>).
    Returns None on unexpected status.
    """
    _, code_challenge = make_pkce_pair()
    params = {
        "client_id":            client_id,
        "redirect_uri":         VALID_REDIRECT_URI,
        "scope":                "ZohoAnalytics.fullaccess.all",
        "state":                "statevalue1",
        "code_challenge":       code_challenge,
        "code_challenge_method": "S256",
    }
    r = requests.get(AUTHORIZE_URL, params=params, allow_redirects=False)
    if r.status_code != 302:
        return None
    location = r.headers.get("Location", "")
    qs       = parse_qs(urlparse(location).query)
    return qs.get("transaction_id", [None])[0]


def get_csrf_token(session: requests.Session, transaction_id: str) -> str | None:
    """
    Use the provided session to GET /consent and extract the CSRF token from
    the hidden form field in the returned HTML.
    The session cookie is updated in-place so the CSRF token remains valid for
    subsequent POST requests within the same session.
    """
    r = session.get(CONSENT_URL, params={"transaction_id": transaction_id})
    if r.status_code != 200:
        return None
    match = _CSRF_RE.search(r.text)
    if not match:
        return None
    return match.group(1) or match.group(2)


# ── /consent (GET) rate-limit tests ──────────────────────────────────────────

def test_consent_rate_limit() -> bool:
    """
    Exhaust the standard rate-limit bucket on GET /consent and verify 429.

    A fresh transaction_id is obtained once; subsequent requests with the same
    (already-invalid-after-expiry but syntactically valid) id still count
    toward the rate limiter because the limiter runs before the transaction
    lookup.
    """
    print("\n=== GET /consent Rate Limit Test ===")
    client         = register_client()
    transaction_id = get_transaction_id(client["client_id"])
    assert transaction_id, "Could not obtain a transaction_id via /authorize"

    for i in range(STANDARD_RATE_LIMIT_COUNT + 3):
        r = requests.get(CONSENT_URL, params={"transaction_id": transaction_id})
        print(f"  Request {i+1}: status={r.status_code}")
        if r.status_code == 429:
            print("  ✓ Rate limit triggered as expected")
            return True

    print("  ✗ Rate limit was NOT triggered after all requests")
    return False


def test_consent_tokens_refill_after_window():
    """Exhaust the /consent bucket, wait for it to refill, confirm success."""
    print(f"\n=== GET /consent Token Refill After {STANDARD_RATE_LIMIT_WINDOW}s Window ===")
    client         = register_client()
    transaction_id = get_transaction_id(client["client_id"])
    assert transaction_id, "Could not obtain a transaction_id via /authorize"

    for i in range(STANDARD_RATE_LIMIT_COUNT + 1):
        r = requests.get(CONSENT_URL, params={"transaction_id": transaction_id})
        print(f"  Exhaust {i+1}: {r.status_code}")

    wait = STANDARD_RATE_LIMIT_WINDOW + 5
    print(f"  Waiting {wait}s for bucket to refill...")
    time.sleep(wait)

    # New transaction for the post-refill check
    client2        = register_client()
    transaction_id2 = get_transaction_id(client2["client_id"])
    assert transaction_id2, "Could not obtain post-refill transaction_id"

    for i in range(2):
        r = requests.get(CONSENT_URL, params={"transaction_id": transaction_id2})
        print(f"  Post-refill {i+1}: {r.status_code}")
        assert r.status_code in (200, 400), (
            f"Expected 200 or 400 after refill, got {r.status_code}"
        )
    print("  ✓ Bucket refilled correctly after window")


def test_different_ip_independent_limits_consent():
    """
    Validate per-IP bucket isolation for /consent via X-Forwarded-For.
    See authorize_endpoint.py for proxy configuration requirements.
    """
    print("\n=== GET /consent Per-IP Independent Limits ===")
    print("  ⚠ Requires BEHIND_PROXY=True and the test host in TRUSTED_PROXY_LIST")

    client         = register_client()
    transaction_id = get_transaction_id(client["client_id"])
    assert transaction_id, "Could not obtain a transaction_id via /authorize"

    headers_ip1 = {"X-Forwarded-For": "10.0.0.3"}
    headers_ip2 = {"X-Forwarded-For": "10.0.0.4"}

    for i in range(STANDARD_RATE_LIMIT_COUNT + 1):
        r = requests.get(
            CONSENT_URL,
            params={"transaction_id": transaction_id},
            headers=headers_ip1,
        )
        print(f"  IP1 request {i+1}: {r.status_code}")

    r = requests.get(
        CONSENT_URL,
        params={"transaction_id": transaction_id},
        headers=headers_ip2,
    )
    print(f"  IP2 request after IP1 exhausted: {r.status_code}")
    if r.status_code in (200, 400):
        print("  ✓ Per-IP rate limiting working (IP2 unaffected by IP1)")
    else:
        print("  ⚠ IP2 was also blocked — rate limiter may be global, not per-IP")


